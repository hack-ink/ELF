use crate::cli::Args;

pub(super) fn render_preamble(args: &Args, out: &mut String) {
	out.push_str("-- Generated by `elf-eval trace_gate_export`.\n");
	out.push_str(&format!(
		"-- trace_ids: {}\n",
		args.trace_id.iter().map(|id| id.to_string()).collect::<Vec<_>>().join(", ")
	));
	out.push_str("BEGIN;\n\n");
}
